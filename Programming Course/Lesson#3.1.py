########### Creating a List ###########

loveq = ["Noemi", 'Noems', 'Naomi', 'my one & only bb']
#the list are separated in commas

print(loveq)
print(len(loveq))

#accessing the valeus in list: counting starts with 0 from left
print(loveq[1])
print(loveq[-1])    # -1 always gets the last item
print(loveq[0:2])   # accessing by range
                    # notes: the first is included, but the end point is exclusive

print(loveq[:4])              
print(loveq[0:])    